function getQuoteOfTheDay() {
    var todayDate = new Date();
    var startDate = new Date(todayDate.getFullYear(), 0, 0);
    var diff = todayDate - startDate;
    var oneDay = 1000 * 60 * 60 * 24;
    var day = Math.floor(diff / oneDay);
    return quotes[day - 1];
}

